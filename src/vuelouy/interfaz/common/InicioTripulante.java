    private void cargarPaneles() {
        menu_Mensaje = new MenuMensajes(comunicacion);
        menu_Pedidos = new MenuListaPedidos(viaje, comunicacion);
        menu_Configuracion = new MenuConfiguracionTripulante(viaje);

        jLayeredTripulante.setSize(589, 578);
        jLayeredTripulante.setVisible(true);

    }

    private void mostrar(JPanel menu) {

        jLayeredTripulante.removeAll();
        menu.setSize(585, 548);
        jLayeredTripulante.add(menu);

        ocultarPaneles();
        menu.setVisible(true);
    }

    private void ocultarPaneles(){
        menu_Mensaje.setVisible(false);
        menu_Pedidos.setVisible(false);
        menu_Configuracion.setVisible(false);
    }
    
    public void notificar(boolean n){
        if (n) {
            btnPedidos.setIcon(new javax.swing.ImageIcon(getClass().getResource("/vuelouy/image/PedidosN.png")));
        } else  {
            btnPedidos.setIcon(new javax.swing.ImageIcon(getClass().getResource("/vuelouy/image/Pedidos.png")));
        }
    }
